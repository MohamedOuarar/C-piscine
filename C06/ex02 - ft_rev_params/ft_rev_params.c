#include <unistd.h>

int	main(int argc, char *argv[])
{
	int	j;

	argc -= 1;
	while (argc > 0)
	{
		j = 0;
		while (argv[argc][j])
		{
			write(1, &argv[argc][j], 1);
			j++;
		}
		write(1, "\n", 1);
		argc--;
	}
}
